class PizzaView(View):
    """Vue contenant un bouton pour gÃ©nÃ©rer une nouvelle pizza."""
    def __init__(self, data: dict):
        super().__init__(timeout=60)
        self.data = data
        self.message = None

    async def on_timeout(self):
        """DÃ©sactive le bouton quand la vue expire."""
        for child in self.children:
            child.disabled = True
        if self.message:
            await safe_edit(self.message, view=self)

    @button(label="ğŸ• Nouvelle pizza", style=discord.ButtonStyle.green)
    async def nouvelle_pizza(self, interaction: discord.Interaction, button: discord.ui.Button):
        try:
            embed = generate_pizza_embed(self.data)
            await safe_edit(interaction.message, embed=embed, view=self)
            await interaction.response.defer()
        except Exception as e:
            print(f"[ERREUR bouton pizza] {e}")
            await interaction.response.send_message("âŒ Erreur lors de la gÃ©nÃ©ration de la pizza.", ephemeral=True)

